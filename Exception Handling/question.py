# 1. What will be the output for print(5 - 'A')?

# It will raise a TypeError
# Because you cannot subtract a string 'A' from an integer 5.




# 2. Which block is always executed irrespective of the exception?

# finally block
# It runs no matter what, whether there's an exception or not.




# 3. Which block is executed when there is no exception?

# else block
# It runs only if no exception occurs in the try block.




# 4. Which is the base class for all the exception classes?

# BaseException
# All built-in exceptions are derived from this class.




# 5. Do we need any module to implement exception handling?

#  No
# Exception handling in Python uses built-in keywords like try, except, finally, and does not require importing any module.


